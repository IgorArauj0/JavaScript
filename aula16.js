
//Métodos são funções que já existem dentro do array.
//Métodos de Array (push, pop, length)
//Por que é fundamental?
//Depois que o aluno aprende Array, ele precisa aprender a manipular a lista.


// Criando um array de cursos
let cursos = ["JS", "Python", "Banco de Dados"];

// push adiciona item no final
cursos.push("React");

// pop remove o último item
cursos.pop();

// length mostra o tamanho do array
console.log(cursos.length);


