//Arrow Function (Função Moderna)
//Introduzir sintaxe moderna do JavaScript.

// Função tradicional
function somar(a, b) {
    return a + b;
}

// Arrow function equivalente
const somar2 = (a, b) => {
    return a + b;
};

console.log(somar2(5, 3));


//Versão ainda mais curta:
const multiplicar = (a, b) => a * b;

console.log(multiplicar(4, 5));